    private void btnIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarActionPerformed
        if (inpNome.getText() != null && inpSaldo.getText() != null) {
            if (Double.valueOf(inpSaldo.getText()) < 0) {
                lblMsg.setText("O valor tem que ser no minimo 0");
            } else {
                ContaCorrente cc = new ContaCorrente(inpNome.getText(),Double.valueOf(inpSaldo.getText()));
                TelaContaCorrente tcc = new TelaContaCorrente(cc);
                tcc.setVisible(true);
                dispose();
            }
        } else {
            lblMsg.setText("VocÃª tem de preencher todos os campos");
        }
    }//GEN-LAST:event_btnIniciarActionPerformed

